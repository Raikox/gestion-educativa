    private void tblNivelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNivelMouseClicked
        int reg;
        DefaultTableModel dtm = new DefaultTableModel();
        reg = tblNivel.getSelectedRow();
        if(reg == -1)
        {
        JOptionPane.showMessageDialog(null, "Debe Seleccionar un registro");
        }
        else
        {
        dtm = (DefaultTableModel)tblNivel.getModel();
        nivelCodigo = ((String) dtm.getValueAt(reg, 0));        
        txtNombre.setText((String) dtm.getValueAt(reg, 1));
        
        txtNombre.setEnabled(true); 
        activaBotones(false,true);
        
        
        }
    }//GEN-LAST:event_tblNivelMouseClicked

