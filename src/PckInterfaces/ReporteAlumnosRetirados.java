    private void CargarComboNivel() {       
        matriculaModelo.CargarComboNivel(cmbNivel);
    }
    
    private void CargarComboPeriodo() {        
        matriculaModelo.CargarComboPeriodo(cmbPeriodo, cmbNivel);
    }
    
    private void CargarComboAula() {
        matriculaModelo.CargarComboAula(cmbAula, cmbPeriodo);
    }
    
    private void CargarTablaRetiradosAula() {
        
        AulaEntity ae = (AulaEntity) cmbAula.getSelectedItem();
        idAula = ae.getId_seccion();
        try {
            alumnoModelo.CargarTablaRetiradosAula(tblRetirados, String.valueOf(idAula));
        } catch (ParseException ex) {
            Logger.getLogger(ReporteAlumnosRetirados.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void CargarTablaRetiradosPeriodo() {
        
        PeriodoEntity pe = (PeriodoEntity) cmbPeriodo.getSelectedItem();
        idPeriodo = pe.getId_periodo();
        try {
            alumnoModelo.CargarTablaRetiradosPeriodo(tblRetirados, String.valueOf(idPeriodo));
        } catch (ParseException ex) {
            Logger.getLogger(ReporteAlumnosRetirados.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
