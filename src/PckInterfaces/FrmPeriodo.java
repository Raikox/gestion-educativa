    private void tblPeriodoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPeriodoMouseClicked

    int reg;
    DefaultTableModel defaultTableModel = new DefaultTableModel();
    reg = tblPeriodo.getSelectedRow();
    if(reg == -1)
    {
        JOptionPane.showMessageDialog(null, "Debe Seleccionar un registro");
    }
    else{
        try {
            defaultTableModel = (DefaultTableModel)tblPeriodo.getModel();
            strCodigo = ((String)defaultTableModel.getValueAt(reg, 0));
            jdcInicio.setDate(formato.parse((String) defaultTableModel.getValueAt(reg, 2)));
            jdcFin.setDate(formato.parse((String) defaultTableModel.getValueAt(reg, 3)));
            txtPerNombre.setText((String)defaultTableModel.getValueAt(reg, 1));
            //Nivel            
            cmbNivel.setSelectedItem((String)defaultTableModel.getValueAt(reg, 4));           
            //Estado
            switch (((String)defaultTableModel.getValueAt(reg, 5))) {
                case "Activo":
                    rdPerActivo.setSelected(true);
                    break;
                case "Inactivo":
                    rdPerInactivo.setSelected(true);
                    break;
            }
        } catch (ParseException ex) {
            Logger.getLogger(FrmPeriodo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        //se llenan los campos de montos
        ClsPeriodo per = new ClsPeriodo();
        try {
            ResultSet rs = per.SeleccionarPeriodo(strCodigo);
            
            while (rs.next())
            {
                txtMatricula.setText(rs.getString("matricula_periodo"));
                txtESa.setText(rs.getString("hora_periodo"));
                txtESb.setText(rs.getString("horb_periodo"));
                txtESc.setText(rs.getString("horc_periodo"));
                txtAlimentacion.setText(rs.getString("alimentacion_periodo"));
                txtDia.setText(rs.getString("alimentacion_dia"));
            }            
        } catch (Exception ex) {
            Logger.getLogger(FrmPeriodo.class.getName()).log(Level.SEVERE, null, ex);
        }        
    }                  
      activaBotones(true,false,true,true);
      activaCampos();
    }//GEN-LAST:event_tblPeriodoMouseClicked

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(txtPerNombre.getText().equals("") || txtMatricula.getText().equals("") || txtESa.getText().equals("") ||
                txtESb.getText().equals("")|| txtESc.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Por favor llene los campos necesarios");
            txtPerNombre.requestFocus();
        }         
        else
        {
                        
            ClsPeriodo periodos = new ClsPeriodo();
            ClsEntidadPeriodo periodo = new ClsEntidadPeriodo();
            periodo.setNombre_Periodo(txtPerNombre.getText());      
            periodo.setInicio_Periodo(formato.format(jdcInicio.getDate())); 
            periodo.setFin_Periodo(formato.format(jdcFin.getDate())); 
            
            //obtiene "2015" de la fecha de inicio
            String anio = formato.format(jdcInicio.getDate());
            periodo.setAnio_Periodo(anio.substring(6));            
            
            if (rdPerActivo.isSelected())
            {
                periodo.setEstado_Periodo(rdPerActivo.getText());
            }
            else if (rdPerInactivo.isSelected())
            {
                periodo.setEstado_Periodo(rdPerInactivo.getText());
            }
            //codigo nivel se obtiene del arrayList
            periodo.setNivel_Id(codigoNivel.get(cmbNivel.getSelectedIndex())); 
            periodo.setMatricula_Periodo(txtMatricula.getText());
            periodo.setHorA_Periodo(txtESa.getText());
            periodo.setHorB_Periodo(txtESb.getText());
            periodo.setHorC_Periodo(txtESc.getText());
            periodo.setAlimentacion_Periodo(txtAlimentacion.getText()); 
            periodo.setAlimentacion_Dia(txtDia.getText()); 
            periodos.AgregarPeriodo(periodo);
            desactivaCampos();  
            activaBotones(true,false,false,true);
            actualizarTabla();
            limpiaCampos();

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

