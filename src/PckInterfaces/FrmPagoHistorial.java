    private void ActualizarTablaHistorial()
    {
        ClsPagoHistorial pagoHistorial = new ClsPagoHistorial();
        String titulos[] = {"ID","Apellidos y Nombres","Concepto","Importe","Fecha"};
        String fila[] = new String[6];
        ResultSet rsPagoHistorial;
        Connection con = ClsConexion.getConection();
        DefaultTableModel dtm = new DefaultTableModel(null,titulos)
        {
            @Override
            public boolean isCellEditable(int rowIndex,int columnIndex){return false;}
        };
        
        try 
        {
            rsPagoHistorial = pagoHistorial.SeleccionarPagoHistorial(con);
            while(rsPagoHistorial.next())
            {
                fila[0] = rsPagoHistorial.getString("id_ha");
                fila[1] = rsPagoHistorial.getString("nombres");
                fila[2] = rsPagoHistorial.getString("concepto_ha");
                fila[3] = rsPagoHistorial.getString("importe_ha");
                fila[4] = rsPagoHistorial.getString("fecha_ha");     
                dtm.addRow(fila);
            }
            
            tblHistorial.setModel(dtm);
            tblHistorial.removeColumn(tblHistorial.getColumnModel().getColumn(0));
            
            TableColumn  cfecha =tblHistorial.getColumnModel().getColumn(0);
            cfecha.setPreferredWidth(220);
            
            TableColumn  cfechaa =tblHistorial.getColumnModel().getColumn(1);
            cfechaa.setPreferredWidth(150);
            
            TableCellRenderer tcr =  tblHistorial.getTableHeader().getDefaultRenderer();                       
            ClsMisc.miRender mr = new ClsMisc.miRender(tcr);
            mr.getTableCellRendererComponent(tblHistorial, fila, isSelected, isIcon, WIDTH, 0);
            mr.getTableCellRendererComponent(tblHistorial, fila, isSelected, isIcon, WIDTH, 1);
            mr.getTableCellRendererComponent(tblHistorial, fila, isSelected, isIcon, WIDTH, 2);
            mr.getTableCellRendererComponent(tblHistorial, fila, isSelected, isIcon, WIDTH, 3);
            
        } catch (Exception ex) {
            ClsConexion.closeConnection(con);
            Logger.getLogger(FrmPagoHistorial.class.getName()).log(Level.SEVERE, null, ex);            
        } finally {
            ClsConexion.closeConnection(con);
        }
        
    }

