    private void actualizarTabla()
    {        
        String titulos[] = {"ID","Nombre"};
        ClsNivel niveles = new ClsNivel();
        ArrayList<ClsEntidadNivel> nivel = niveles.SeleccionarNivel();
        Iterator it = nivel.iterator();
        DefaultTableModel dtm = new DefaultTableModel(null,titulos){ 
        public boolean isCellEditable(int rowIndex,int columnIndex){return false;} 
        };    
        String fila[] = new String[3];
        while(it.hasNext())
        {
            ClsEntidadNivel Nivel = new ClsEntidadNivel();            
            Nivel = (ClsEntidadNivel) it.next();            
            fila[0] = Nivel.getNivel_id();
            fila[1] = Nivel.getNivel_nombre();     
            dtm.addRow(fila);
            
        }
        tblNivel.setModel(dtm);
        tblNivel.removeColumn(tblNivel.getColumnModel().getColumn(0)); 
        
        //CENRAR COLUMNAS Y FILAS (ClsMisc)
        TableCellRenderer tcr =  tblNivel.getTableHeader().getDefaultRenderer();                       
        miRender mr = new miRender(tcr);
        mr.getTableCellRendererComponent(tblNivel, fila, isSelected, isIcon, WIDTH, 0);
        //public boolean isCellEditable(int rowIndex, int columnIndex) {
        //if (columnIndex==4) {
        //return true;  //La columna 4 es editable.
        //}
        //return false;  //El resto de celdas no son editables.
        //}
    }
    
