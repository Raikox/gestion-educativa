    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
         //Pasando par√°metros
        ini = jdcInicio.getDate();
        fi = jdcFin.getDate();
        String url ="/Reportes/RptBoletas.jasper";
        String auxi = formato.format(ini);
        String auxf = formato.format(fi);
        String total = "";
        ClsBoleta bol = new ClsBoleta();
        ResultSet tot;
        
        try 
        {
            con = ClsConexion.getConection();
            tot = bol.SeleccionarTotalBoleta(auxi, auxf, con);
            while(tot.next())
            {
                total = tot.getString("monto_histo");
            }
        } 
        catch (Exception ex) 
        {
            Logger.getLogger(FrmRptBoleta.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        JasperReport report;
                Map p = new HashMap();            
                 p.put("inicio", auxi);
                 p.put("fin", auxf);
                 p.put("total", total);
            JasperPrint print;

            try{
                print = JasperFillManager.fillReport(System.getProperty("user.dir")+url,p ,con);
                JasperViewer view = new JasperViewer(print,false);

                view.setTitle("Reporte General de Boletas");
                view.setVisible(true);
            }
            catch(JRException ex)
            {
                ex.printStackTrace();
                //JOptionPane.showMessageDialog(this, ex);
            }
    }//GEN-LAST:event_btnGenerarActionPerformed

