public class ClsEntidadServicio {
private String Id_Servicio;
private String Nombre_Servicio;
private String Precio_Servicio;
private String Descripcion_Servicio;
private String Anio_Servicio;

    public String getId_Servicio() {
        return Id_Servicio;
    }

    public void setId_Servicio(String Id_Servicio) {
        this.Id_Servicio = Id_Servicio;
    }

    public String getNombre_Servicio() {
        return Nombre_Servicio;
    }

    public void setNombre_Servicio(String Nombre_Servicio) {
        this.Nombre_Servicio = Nombre_Servicio;
    }

    public String getPrecio_Servicio() {
        return Precio_Servicio;
    }

    public void setPrecio_Servicio(String Precio_Servicio) {
        this.Precio_Servicio = Precio_Servicio;
    }

    public String getDescripcion_Servicio() {
        return Descripcion_Servicio;
    }

    public void setDescripcion_Servicio(String Descripcion_Servicio) {
        this.Descripcion_Servicio = Descripcion_Servicio;
    }

    public String getAnio_Servicio() {
        return Anio_Servicio;
    }

    public void setAnio_Servicio(String Anio_Servicio) {
        this.Anio_Servicio = Anio_Servicio;
    }


}
