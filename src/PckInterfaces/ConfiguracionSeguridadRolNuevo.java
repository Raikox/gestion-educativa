    private void ValidarCampos()
    {        
        ((AbstractDocument) txtNombre.getDocument()).setDocumentFilter(filtroMayuscula);
        ((AbstractDocument) txtDescripcion.getDocument()).setDocumentFilter(filtroMayuscula);
    }
    
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        try {
            Connection con = ClsConexion.getConection();
            String idRol = "";
            
            seguridadDAL.GuardarRol(
                    txtNombre.getText(),
                    txtDescripcion.getText(),
                    con
            );
            
            idRol = ClsMisc.UltimoIdInsertado(con);
            
            seguridadDAL.GuardarRolAula(idRol, con);
            seguridadDAL.GuardarRolMatricula(idRol, con);
            seguridadDAL.GuardarRolAdministracion(idRol, con);
            seguridadDAL.GuardarRolProductoServicio(idRol, con);
            seguridadDAL.GuardarRolContabilidad(idRol, con);
            seguridadDAL.GuardarRolHerramienta(idRol, con);
            seguridadDAL.GuardarRolSeguridad(idRol, con);
            
            ClsConexion.closeConnection(con);
            
            configuracionSeguridad.CargarTablaRoles();

            this.dispose();       
            
        } catch (Exception ex) {
            Logger.getLogger(ConfiguracionSeguridadRolNuevo.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
    }//GEN-LAST:event_btnGuardarActionPerformed

