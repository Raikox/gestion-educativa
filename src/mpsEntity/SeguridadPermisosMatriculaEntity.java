public class SeguridadPermisosMatriculaEntity {

    String rol_id;
    String matricula;
    String huella;
    String nivel;
    String periodo;
    String aula;

    public SeguridadPermisosMatriculaEntity(String matricula, String huella, String nivel, String periodo, String aula) {
        this.matricula = matricula;
        this.huella = huella;
        this.nivel = nivel;
        this.periodo = periodo;
        this.aula = aula;
    }
    
    public String getRol_id() {
        return rol_id;
    }

    public void setRol_id(String rol_id) {
        this.rol_id = rol_id;
    }

    public String getMatricula() {
        return matricula;
    }

    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }

    public String getHuella() {
        return huella;
    }

    public void setHuella(String huella) {
        this.huella = huella;
    }

    public String getNivel() {
        return nivel;
    }

    public void setNivel(String nivel) {
        this.nivel = nivel;
    }

    public String getPeriodo() {
        return periodo;
    }

    public void setPeriodo(String periodo) {
        this.periodo = periodo;
    }

    public String getAula() {
        return aula;
    }

    public void setAula(String aula) {
        this.aula = aula;
    }
    
    
}
