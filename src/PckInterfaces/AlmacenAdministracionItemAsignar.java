    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        int column = tblAlmacenes.getSelectedColumn();
        int row = tblAlmacenes.getSelectedRow();

        if(row != -1)
        {
            //varios almacenes a la vez
            int FilasAlmacenSeleccionadas[] = tblAlmacenes.getSelectedRows();
            int IdAlmacenSeleccionados[] = new int[FilasAlmacenSeleccionadas.length];
            DefaultTableModel dtmAlmacen = (DefaultTableModel) tblAlmacenes.getModel();
             
            //llena los ID de almacen seleccionados
            if(FilasAlmacenSeleccionadas.length != 0)
            {
                for (int k= 0; k<FilasAlmacenSeleccionadas.length; k++)
                {
                    IdAlmacenSeleccionados[k] = (int)dtmAlmacen.getValueAt(FilasAlmacenSeleccionadas[k], 0);
                }
            }
            
            DefaultTableModel dtmAlmacenes = (DefaultTableModel) tblAlmacenes.getModel();            
            int limiteItem = _IdSeleccionados.length;
            int limiteAlmacen = IdAlmacenSeleccionados.length;
                        
            if(cbxNuevoItem.isSelected()) //Crea un item y lo asigna a un almacen
            {
                try
                {
                    for(int a : IdAlmacenSeleccionados)
                    {
                        for(int i=0; i < limiteItem; i++)
                        {
                            //Crea nuevo registros y asigna almacen
                            int lastId = itemDAL.GuardarItem(_Nombre[i], _Medida[i], _Stock[i], _Descripcion[i],_Tipo[i]);
                            itemDAL.EditarItemAlmacen(lastId, a);
                            itemStockDAL.GuardarItemCantidad(fechaActual(), String.valueOf(_Stock[i]), 
                                    Integer.parseInt(FrmPrincipal.idPersonalLogeado), lastId );
                        }                        
                    }
                    almacenAdministracionItem.MostrarItems();
                    almacenAdministracionItem.LimpiarCampos();
                    
                    this.dispose();
                }
                catch(HeadlessException ex)
                {
                    Logger.getLogger(AlmacenAdministracionItemAsignar.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            else //Asigna el item a un almacen
            {

                try
                {
                    for(int a : IdAlmacenSeleccionados)
                    {
                        for(int i=0; i < limiteItem; i++)
                        {
                            itemDAL.EditarItemAlmacen(_IdSeleccionados[i], a);
                        }                         
                    }
                    almacenAdministracionItem.MostrarItems();
                    almacenAdministracionItem.LimpiarCampos();
                    int n = JOptionPane.showConfirmDialog(null, "Â¿Desea asignar otro almacen?", "Confirmar", JOptionPane.OK_CANCEL_OPTION);
                    if(n == JOptionPane.CANCEL_OPTION) 
                    {
                        this.dispose();
                    } 
                }
                catch(HeadlessException ex)
                {
                    Logger.getLogger(AlmacenAdministracionItemAsignar.class.getName()).log(Level.SEVERE, null, ex);
                }
            }  
        }
        
                         
        
    }//GEN-LAST:event_btnGuardarActionPerformed

