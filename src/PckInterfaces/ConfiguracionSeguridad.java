    private void tblRolesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRolesMouseClicked

        int column = tblRoles.getSelectedColumn();
        int row = tblRoles.getSelectedRow();

        if(row != -1)
        {
            if(evt.getClickCount() == 2) {
                
                DefaultTableModel dtmRoles=  (DefaultTableModel) tblRoles.getModel();
                String idRol = String.valueOf(dtmRoles.getValueAt(row, 0));
                String nombreRol = (String) dtmRoles.getValueAt(row, 2);
                String descripcionRol = (String) dtmRoles.getValueAt(row, 3);                         
                AbrirDialogRolEditar(idRol,nombreRol,descripcionRol);
                
            } else if(evt.getClickCount() == 1) {
                
                DefaultTableModel dtmRoles = (DefaultTableModel) tblRoles.getModel();
                String idRol = String.valueOf(dtmRoles.getValueAt(row, 0));
                CargarCheckBoxPermisosAula(idRol);
                CargarCheckBoxPermisosMatricula(idRol);
                CargarCheckBoxPermisosAdministracion(idRol);
                CargarCheckBoxPermisosContabilidad(idRol);
                CargarCheckBoxPermisosHerramientas(idRol);
                CargarCheckBoxPermisosSeguridad(idRol);
            } 
                     
                
        }
    }//GEN-LAST:event_tblRolesMouseClicked

