    private void tblEgresosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEgresosMouseClicked
        int reg;
      DefaultTableModel dtm;
      reg = tblEgresos.getSelectedRow();
        if(reg == -1)
        { JOptionPane.showMessageDialog(null, "Debe Seleccionar un registro"); 
        }
        else
        {
            try {
                dtm = (DefaultTableModel)tblEgresos.getModel();
                strCodigo = ((String)dtm.getValueAt(reg, 0));
                cmbPersona.setSelectedItem(((String)dtm.getValueAt(reg, 1)));
                txtConcepto.setText((String)dtm.getValueAt(reg, 2));                
                txtDescripcion.setText((String)dtm.getValueAt(reg, 3));
                txtMonto.setText((String)dtm.getValueAt(reg, 4));
                txtFecha.setDate(jdcFechaEgreso.getDate());
            } catch (Exception ex) {
                Logger.getLogger(FrmPeriodo.class.getName()).log(Level.SEVERE, null, ex);
            }            
            activaBotones(true,false,true,true);  
            activaCampos(); 
            
            //valida que no tenga permitido editar egreso
            if(!FrmPrincipal.editarEgreso){
                
                if(jdcFechaEgreso.getDate().before(date)) {
                    btnModificar.setEnabled(false);
                }
            }
            
        }
    }//GEN-LAST:event_tblEgresosMouseClicked

