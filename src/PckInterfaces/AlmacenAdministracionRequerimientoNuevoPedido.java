    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       if(!txtCantidad.getText().equals("") && !txtSustento.getText().equals(""))
       {
            int restante = Integer.parseInt(almacenAdministracionRequerimientoEvaluar._StockRestante);
            int pedido = Integer.parseInt(txtCantidad.getText());
            
            if(pedido <= restante)
            {
                almacenAdministracionRequerimientoEvaluar._Cantidad = txtCantidad.getText();
                almacenAdministracionRequerimientoEvaluar._Sustento = txtSustento.getText();
                almacenAdministracionRequerimientoEvaluar.guardar = true;
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "La cantidad solicitada no estÃ¡ disponible, elija un monto menor", 
                        "Error al agregar item", JOptionPane.ERROR_MESSAGE);
            }
            
       }       

    }//GEN-LAST:event_btnGuardarActionPerformed
   
    
