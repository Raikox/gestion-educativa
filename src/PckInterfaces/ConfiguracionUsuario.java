    private void tblUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuariosMouseClicked
        
        int column = tblUsuarios.getSelectedColumn();
        int row = tblUsuarios.getSelectedRow();
        
        if(row != -1) {
            
            DefaultTableModel dtmUsuario = (DefaultTableModel) tblUsuarios.getModel();
            idUsuario = (String) dtmUsuario.getValueAt(row, 0);
            String nombreRol = (String) dtmUsuario.getValueAt(row, 4);
            
            if(evt.getClickCount() == 2) {
                
                AbrirDialogUsuarioEditar(idUsuario,nombreRol);
            }
            else {
                
                try {
                    CargarUsuarioDetalle(idUsuario);
                } catch (ParseException ex) {
                    Logger.getLogger(ConfiguracionUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                CargarUsuarioLogin(idUsuario);
                
                txtUsuario.setEnabled(true);
                txtPassword.setEnabled(true);
                btnGuardar.setEnabled(true);
            }
        }
    }//GEN-LAST:event_tblUsuariosMouseClicked

