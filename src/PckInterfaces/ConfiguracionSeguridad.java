    private String ObetenerIdRol() {
                
        int row = tblRoles.getSelectedRow();
        DefaultTableModel dtmRoles = (DefaultTableModel) tblRoles.getModel();
        String idRol = String.valueOf(dtmRoles.getValueAt(row, 0));
        //System.out.println(idRol);
        return idRol;
    }
    
