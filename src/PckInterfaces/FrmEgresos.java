    private void comboPersona()
    {
        codigoPersonal.clear();
        ClsPersonal personas = new ClsPersonal();
        con = ClsConexion.getConection();
        ArrayList<ClsEntidadPersonal> persona =  personas.SeleccionarPersonalNombre(con);
        Iterator it = persona.iterator();
        DefaultComboBoxModel dcm = new DefaultComboBoxModel();
        dcm.removeAllElements();
        cmbPersona.removeAllItems();
        String fila[] = new String[3];
            
        while(it.hasNext())
        {
            ClsEntidadPersonal Persona = (ClsEntidadPersonal) it.next();
            
            if(Persona.getPersonal_estado().equals("A")) {
                codigoPersonal.add(Persona.getPersonal_id());
                fila[0] = Persona.getPersonal_nombre();
                dcm.addElement(fila[0]);      
            } 
                     
        }
        ClsConexion.closeConnection(con);
        cmbPersona.setModel(dcm);           
    }
    
    
    private void tablaEgreso()
    {
        String titulos[] = {"ID","PERSONA","CONCEPTO","DESCRIPCION","MONTO"};
        ClsIngresosEgresos egresos = new ClsIngresosEgresos();
        con = ClsConexion.getConection();
        String fecha = formato.format(jdcFechaEgreso.getDate());
        ArrayList<ClsEntidadEgresos> egreso = egresos.SeleccionarEgresos(fecha, con);
        Iterator iterator = egreso.iterator();
        DefaultTableModel dtm =new DefaultTableModel(null,titulos){
        public boolean isCellEditable(int rowIndex,int columnIndex){return false;} 
        };         
        String fila[] = new String[6];
        while(iterator.hasNext())
        {
            ClsEntidadEgresos Egreso = new ClsEntidadEgresos();
            Egreso = (ClsEntidadEgresos) iterator.next();
            fila[0] = Egreso.getId_Egreso();
            fila[1] = Egreso.getNombre_Persona();
            fila[2] = Egreso.getConcepto_Egreso();
            fila[3] = Egreso.getDescripcion_Egreso();
            fila[4] = Egreso.getMonto_Egreso();            
            dtm.addRow(fila);
        }
        ClsConexion.closeConnection(con);
        tblEgresos.setModel(dtm);        
        tblEgresos.removeColumn(tblEgresos.getColumnModel().getColumn(0));
        TableColumn  columnaNombre =tblEgresos.getColumnModel().getColumn(0);
                columnaNombre.setPreferredWidth(160);
        TableColumn  columnaApe =tblEgresos.getColumnModel().getColumn(1);
        columnaApe.setPreferredWidth(160);
        TableCellRenderer tcr =  tblEgresos.getTableHeader().getDefaultRenderer();  
        miRender mr = new miRender(tcr);       
        mr.getTableCellRendererComponent(tblEgresos, fila, isSelected, isIcon, WIDTH, WIDTH);
    }
    
    
