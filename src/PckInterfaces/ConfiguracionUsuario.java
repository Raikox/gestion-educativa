    @Override
    public void actionPerformed(ActionEvent e) {
        
        JMenuItem menu = (JMenuItem) e.getSource();
        
        if(menu == mnuDesactivarUsuario) {
            int option = JOptionPane.showConfirmDialog(null, "¿Seguro que desea desactivar el usuario?","Confirmacion",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            
            if(option == 0) {
                usuarioModelo.EditarUsuarioEstado(idUsuario, "I");
                CargarTablaUsuarios();
                LimpiarCampos();
            }            
        }
    }
