    private void actualizarTabla()
    {
        
        String titulos[] = {"Codigo","Nombre","Inicio","Fin","Nivel","Estado"};
        ClsPeriodo periodos = new ClsPeriodo();
        ArrayList<ClsEntidadPeriodo> periodo = periodos.ListarPeriodo();
        Iterator iterator = periodo.iterator();
        DefaultTableModel defaultTableModel =new DefaultTableModel(null,titulos){
        public boolean isCellEditable(int rowIndex,int columnIndex){return false;}
        }; 
        
        String fila[] = new String[6];
        while(iterator.hasNext()){
            ClsEntidadPeriodo Periodo = new ClsEntidadPeriodo();
            Periodo = (ClsEntidadPeriodo) iterator.next();
            fila[0] = Periodo.getId_Periodo();
            fila[1] = Periodo.getNombre_Periodo();  
            fila[2] = Periodo.getInicio_Periodo();
            fila[3] = Periodo.getFin_Periodo();
            fila[4] = Periodo.getNivel_Periodo();            
            fila[5] = Periodo.getEstado_Periodo();
            
            defaultTableModel.addRow(fila);
        }
        tblPeriodo.setModel(defaultTableModel);
        tblPeriodo.removeColumn(tblPeriodo.getColumnModel().getColumn(0));
  
    }
    
